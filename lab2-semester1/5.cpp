//C++  Program to Calculate Area of Rectangle

#include <iostream>

using namespace std;

int main()
{
    float w, l, area;
    cout<<"Input the width of rectangle. a = ";
    cin>>w;
    cout<<"Input the length of rectangle. b = ";
    cin>>l;
    
    area=w*l;
    
    cout<<"Area of Rectangle = "<<area<<endl;

    system("pause");
    return 0;
}
